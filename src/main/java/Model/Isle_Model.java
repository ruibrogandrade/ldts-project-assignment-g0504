package Model;

public class Isle_Model {
    private int x;
    private int y;

    public Isle_Model() {}

    public Isle_Model(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
