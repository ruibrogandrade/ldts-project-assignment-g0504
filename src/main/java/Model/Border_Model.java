package Model;

public class Border_Model {
    private int x;
    private int y;

    public Border_Model(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
            return x;
        }

    public int getY() {
            return y;
        }

    public void setX(int x) {
            this.x = x;
        }

    public void setY(int y) {
            this.y = y;
        }
}
